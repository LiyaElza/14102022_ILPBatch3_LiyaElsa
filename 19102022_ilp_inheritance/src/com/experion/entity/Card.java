package com.experion.entity;

public class Card extends Product {
	private String cardTytpe;

	public Card(String productCode, String productName, String productType, String cardTytpe) {
		super(productCode, productName, productType);
		this.cardTytpe = cardTytpe;
	}

	public String getCardTytpe() {
		return cardTytpe;
	}

	public void setCardTytpe(String cardTytpe) {
		this.cardTytpe = cardTytpe;
	}
	
	

}
